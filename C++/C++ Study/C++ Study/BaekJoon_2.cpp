#include<iostream>
using namespace std;

int main()
{
	int a, b;

	cin >> a >> b;
	cout << "Sum" << endl;
	cout << a + b << endl;
	
	cout << "Sub" << endl;
	cout << a - b;

	cout << "Multi" << endl;
	cout << a * b << endl;
	
	cout << "Div" << endl;
	cout << a / b << endl;

	cout << "Mod" << endl;
	cout << a % b << endl;
}